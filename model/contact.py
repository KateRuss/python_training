class Contact:

    def __init__(self, first_name, last_name, address, home_phone_number, mobile_phone_number,
                           work_phone_number, fax_number, email_1, email_2, email_3):
        self.first_name = first_name
        self.last_name = last_name
        self.address = address
        self.home_phone_number = home_phone_number
        self.mobile_phone_number = mobile_phone_number
        self.work_phone_number = work_phone_number
        self.fax_number = fax_number
        self.email_1 = email_1
        self.email_2 = email_2
        self.email_3 = email_3